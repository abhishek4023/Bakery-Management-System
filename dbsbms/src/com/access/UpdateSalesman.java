    private void cmbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbActionPerformed
        // TODO add your handling code here:
        
        uid=(String)cmb.getSelectedItem();
        String strsql="select * from sprofile where userid=?";
        String strpass="select userpass from login where userid=?";
        try{
            
            psinfo=con.prepareStatement(strsql);
            psinfo.setString(1, uid);
            rsinfo=psinfo.executeQuery();
            rsinfo.next();
            String name=rsinfo.getString("name");
            String addr=rsinfo.getString("address");
            String phno=rsinfo.getString("phno");
            String email=rsinfo.getString("email");
            txtname.setText("  " +name);
            txtaddr.setText(addr);
            txtphno.setText(phno);
            txtemail.setText(email);
            
            pspass=con.prepareStatement(strpass);
            pspass.setString(1, uid);
            rspass=pspass.executeQuery();
            rspass.next();
            String pass=rspass.getString("userpass");
            txtpass.setText(pass);
            
        }
        catch(SQLException se)
        {
            System.out.println(se);
        }
    }//GEN-LAST:event_cmbActionPerformed

