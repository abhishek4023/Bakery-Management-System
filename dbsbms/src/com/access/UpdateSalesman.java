    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name=txtname.getText();
        String addr=txtaddr.getText();
        String phno=txtphno.getText();
        String email=txtemail.getText();
        String pass=txtpass.getText();
        if(checkEmpty(name,addr,phno,pass,email))
        {
            JOptionPane.showMessageDialog (this, "You have left one or more Fields BLANK", "ALERT!!!", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
        String strupdate="update sprofile set name=? , address=? , phno=?, email=? where userid=?";
        String strpassupdate="update login set userpass=? where userid=?";
        try{
            
            psupdate=con.prepareStatement(strupdate);
            psupdate.setString(1, name);
            psupdate.setString(2, addr);
            psupdate.setString(3, phno);
            psupdate.setString(4, email);
            psupdate.setString(5, uid);
            int rw=psupdate.executeUpdate();
            
            psloginupdate=con.prepareStatement(strpassupdate);
            psloginupdate.setString(1, pass);
            psloginupdate.setString(2, uid);
            int rw2=psloginupdate.executeUpdate();
            
            if(rw>0 && rw>0)
            {
                JOptionPane.showMessageDialog(this, "Your Details are Updated");
            }
            
        }
        catch(SQLException se)
        {
            System.out.println(se);
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    }
    private void txtphnoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtphnoFocusLost
        // TODO add your handling code here:
         String ph=txtphno.getText();
        char[]ch=ph.toCharArray();
        int j=0;
        if(ph.length()!=10)
        {
            JOptionPane.showMessageDialog(this, "You Entered Wrong phone number");
        }
       
        for(int i=0; i<ph.length(); i++)
        {
            if(ch[i]<48 || ch[i]>57)
            {
                j++;
            }
        }
        
        if(j!=0)
        {
                   JOptionPane.showMessageDialog(this, "Only Numerical Values should be Entered");

        }
    }//GEN-LAST:event_txtphnoFocusLost

