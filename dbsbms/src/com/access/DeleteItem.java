     public void fillCombo()
    {
        String strsql="select pid from products";
        
        try{
            
            pscombo=con.prepareStatement(strsql);
            rscombo=pscombo.executeQuery();
            
            while(rscombo.next())
            {
                String data =rscombo.getString("pid");
                cmb.addItem(data);
            }           
        }
        
        catch(SQLException se)
        {
            System.out.println(se);
        }
    }

